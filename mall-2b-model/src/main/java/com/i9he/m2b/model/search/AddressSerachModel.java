package com.i9he.m2b.model.search;

public class AddressSerachModel {
	
	private String province;
	
	private String city;
	
	private String county;
	
	private Integer provinceNo;
	
	private Integer cityNo;
	
	private Integer countyNo;

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getCounty() {
		return county;
	}

	public void setCounty(String county) {
		this.county = county;
	}

	public Integer getProvinceNo() {
		return provinceNo;
	}

	public void setProvinceNo(Integer provinceNo) {
		this.provinceNo = provinceNo;
	}

	public Integer getCityNo() {
		return cityNo;
	}

	public void setCityNo(Integer cityNo) {
		this.cityNo = cityNo;
	}

	public Integer getCountyNo() {
		return countyNo;
	}

	public void setCountyNo(Integer countyNo) {
		this.countyNo = countyNo;
	}
	
}
